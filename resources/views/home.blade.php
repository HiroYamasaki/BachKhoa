}
function easeOut2(t) { return 1 - Math.pow(1 - t, 3); }
function animateDonut2(el) {
var pct    = parseInt(el.getAttribute('data-percent'), 10) || 0;
var bar    = el.querySelector('.bkd-donut-bar');
var val    = el.querySelector('.bkd-donut-val');
