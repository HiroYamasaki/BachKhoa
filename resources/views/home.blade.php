if (!bar || !val || !runner || !rdot) return;
var start  = performance.now();
(function step(ts) {
var t   = Math.min((ts - start) / 1500, 1);
var ep  = easeOut2(t);
var cur = ep * pct;
var fill = C2 * cur / 100;
bar.setAttribute('stroke-dasharray', fill + ' ' + (C2 - fill));
val.firstChild.textContent = Math.round(cur);
